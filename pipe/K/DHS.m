function [N_s,N_t]=DHS(s,t)
%形函数求导
%ξ,η
N_s(1)=-1/4*(1-t)*(-s-t-1)-1/4*(1-s)*(1-t);
N_s(2)=1/4*(1-t)*(s-t-1)+1/4*(1+s)*(1-t);
N_s(3)=1/4*(1+t)*(s+t-1)+1/4*(1+s)*(1+t);
N_s(4)=-1/4*(1+t)*(-s+t-1)-1/4*(1-s)*(1+t);

N_s(5)=1/2*(1-s)*(1-t)-1/2*(1+s)*(1-t);
N_s(6)=1/2*(1+t)*(1-t);
N_s(7)=1/2*(1-s)*(1+t)-1/2*(1+s)*(1+t);
N_s(8)=-1/2*(1+t)*(1-t);


N_t(1)=-1/4*(1-s)*(-s-t-1)-1/4*(1-s)*(1-t);
N_t(2)=-1/4*(1+s)*(s-t-1)-1/4*(1+s)*(1-t);
N_t(3)=1/4*(1+s)*(s+t-1)+1/4*(1+s)*(1+t);
N_t(4)=1/4*(1-s)*(-s+t-1)+1/4*(1-s)*(1+t);


N_t(5)=-1/2*(1+s)*(1-s);
N_t(6)=1/2*(1+s)*(1-t)-1/2*(1+s)*(1+t);
N_t(7)=1/2*(1+s)*(1-s);
N_t(8)=1/2*(1-s)*(1-t)-1/2*(1-s)*(1+t);